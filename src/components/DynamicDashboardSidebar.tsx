export default function DynamicDashboardSidebar({ user, config, currentUser }: DynamicDashboardSidebarProps) {
  const [isOpen, setIsOpen] = useState(false);
  const pathname = usePathname();

  const renderSidebarItem = (item: SidebarItem) => {
    // Check if current user can access this item
    const canAccess = canAccessRoute(
      currentUser.role,
      item.href
    );

    if (!canAccess) {
      return null;
    }

    const isActive = pathname === item.href || (item.children && item.children.some(child => pathname === child.href));
    const icon = iconMap[item.icon] || iconMap['folder'];

    return (
      <Link
        key={item.href}
        href={item.href}
        onClick={() => setIsOpen(false)}
        className={`
          flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium transition-colors
          ${isActive 
            ? 'border' 
            : 'text-gray-700 hover:bg-gray-100'
          }
        `}
        style={isActive ? { backgroundColor: 'rgba(0, 24, 86, 0.1)', borderColor: '#001856', color: '#001856' } : {}}
      >
        <span className={isActive ? '' : 'text-gray-500'} style={isActive ? { color: '#001856' } : {}}>
          {icon}
        </span>
        <span className="flex-1">{item.label}</span>
        {item.badge && (
          <span className="px-2 py-0.5 text-xs bg-red-500 text-white rounded-full">
            {item.badge}
          </span>
        )}
      </Link>
    );
  };

  const renderSidebarSection = (section: SidebarSection, index: number) => {
    // Filter items based on permissions
    const accessibleItems = section.items.filter((item: SidebarItem) => 
      canAccessRoute(
        currentUser.role,
        item.href
      )
    );

    if (accessibleItems.length === 0) {
      return null;
    }

    return (
      <div key={index} className="space-y-2">
        {section.title && (
          <h3 className="px-3 text-xs font-semibold text-gray-500 uppercase tracking-wider">
            {section.title}
          </h3>
        )}
        {accessibleItems.map(renderSidebarItem)}
      </div>
    );
  };

  return (
    <>
      {/* Mobile Menu Button */}
      <button
        onClick={() => setIsOpen(!isOpen)}
        className="lg:hidden fixed top-4 left-4 z-50 p-2 rounded-lg bg-white shadow-md border border-gray-200"
      >
        <svg className="w-6 h-6" fill="none" stroke="currentColor" viewBox="0 0 24 24">
          <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M4 6h16M4 12h16M4 18h16" />
        </svg>
      </button>

      {/* Overlay */}
      {isOpen && (
        <div 
          className="lg:hidden fixed inset-0 bg-black bg-opacity-50 z-40"
          onClick={() => setIsOpen(false)}
        />
      )}

      {/* Sidebar */}
      <div className={`
        fixed top-16 left-0 h-[calc(100vh-4rem)] w-64 bg-white border-r border-gray-200 z-40 transform transition-transform duration-300 ease-in-out overflow-y-auto
        ${isOpen ? 'translate-x-0' : '-translate-x-full'}
        lg:translate-x-0 lg:static lg:z-auto
        ${config.layout === 'minimal' ? 'lg:w-16' : 'lg:w-64'}
      `}>
        <nav className="p-4 space-y-6">
          {/* User role indicator */}
          <div className="px-3 py-2 bg-gradient-to-r from-sky-50 to-sky-100 rounded-lg border border-sky-200">
            <p className="text-xs font-medium uppercase tracking-wider" style={{ color: '#001856' }}>
              {user.role.replace(/_/g, ' ')}
            </p>
            <p className="text-sm font-medium mt-1" style={{ color: '#001856' }}>
              {user.fullName}
            </p>
          </div>

          {/* Render sidebar sections */}
          {config.sidebar.sections.map(renderSidebarSection)}

          {/* Role-specific shortcuts */}
          {user.role === 'SUPER_ADMIN' && (
            <div className="pt-4 border-t border-gray-200">
              <h3 className="px-3 text-xs font-semibold text-red-500 uppercase tracking-wider mb-2">
                Admin Tools
              </h3>
              <Link
                href="/dashboard/admin/system"
                className="flex items-center gap-3 px-3 py-2 rounded-lg text-sm font-medium text-red-600 hover:bg-red-50 transition-colors"
              >
                <svg className="w-5 h-5" fill="none" stroke="currentColor" viewBox="0 0 24 24">
                  <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M9.75 17L9 20l-1 1h8l-1-1-.75-3M3 13h18M5 17h14a2 2 0 002-2V5a2 2 0 00-2-2H5a2 2 0 00-2 2v10a2 2 0 002 2z" />
                </svg>
                System Monitor
              </Link>
            </div>
          )}
        </nav>
      </div>
    </>
  );
}
